/**
 * 类型声明和类型推断的区别？
 * 类型声明（Type Annotation）是指在代码中显式地指定变量、函数参数或返回值的类型。通过类型声明，开发者可以明确地告诉编译器某个值应该是什么类型，从而在编译阶段进行类型检查，防止类型错误。例如：
 * let age: number = 25; // 这里明确声明了 age 是一个数字。
 * 类型推断（Type Inference）是指编译器根据变量的初始值自动推断出其类型，而不需要显式地进行类型声明。TypeScript 会根据变量的赋值来推断其类型，从而在后续的代码中进行类型检查。例如：
 * let name = "Alice"; // 这里没有显式声明类型，但编译器会根据赋值自动推断出 name 是一个字符串。
 * 总结：类型声明是显式指定类型，而类型推断是编译器根据初始值自动推断类型。两者都旨在提高代码的类型安全性，但类型声明提供了更明确的类型信息，而类型推断则简化了代码书写。
 */
let age: number = 25; // 这里明确声明了 age 是一个数字。
